/*
01) Crie uma função que dado dois valores (passados como parâmetros) mostre no console a soma, subtração,
multiplicação e divisão desses valores.

*/
function calcularOperacoes (operador1, operador2) {
    
    console.log("A soma é: "+(operador1+operador2));
    console.log("A subtração é: "+(operador1-operador2));
    console.log("A multiplicação é: "+(operador1*operador2));
    console.log("A divisão é: "+(operador1/operador2))
}

calcularOperacoes(10, 5);

